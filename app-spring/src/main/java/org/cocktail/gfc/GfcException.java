package org.cocktail.gfc;

public class GfcException extends RuntimeException {

    public GfcException() {
        super();
    }

    public GfcException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public GfcException(String arg0) {
        super(arg0);
    }

    public GfcException(Throwable arg0) {
        super(arg0);
    }

}
